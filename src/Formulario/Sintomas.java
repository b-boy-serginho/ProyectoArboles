    private String arbolToString(IArbolBusqueda<String, String> arbol) {
        // Aquí debes implementar la lógica para convertir el árbol a una representación de cadena.
        // Este es un ejemplo simple que debes adaptar según tu implementación del árbol.
        StringBuilder sb = new StringBuilder();
        for (String clave : arbol.recorridoEnInOrden()) {
            sb.append(clave).append(": ").append(arbol.buscar(clave)).append("\n");
        }
        return sb.toString();
    }

