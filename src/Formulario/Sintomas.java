    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       JOptionPane.showMessageDialog(null, "Sistema iniciado...");  
        if (rbArbolBinario.isSelected()) {
            arbolPrueba = new ArbolBinarioBusqueda<>();
        } else if (rbAVL.isSelected()) {
            arbolPrueba = new AVL<>();
        } else if (rbArbolMVias.isSelected()) {
            int orden = Integer.parseInt(JOptionPane.showInputDialog("Digite el orden para árbol MVias"));
            try {
                arbolPrueba = new ArbolMViasBusqueda<>(orden);
            } catch (OrdenInvalidoExcepcion ex) {
                Logger.getLogger(Sintomas.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (rbArbolB.isSelected()) {
            try {
                arbolPrueba = new ArbolB<>(3);
            } catch (OrdenInvalidoExcepcion ex) {
                Logger.getLogger(Sintomas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

